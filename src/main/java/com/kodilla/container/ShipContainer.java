package com.kodilla.container;

public class ShipContainer {
    public int shipCounts;

    public ShipContainer() {
        this.shipCounts = 0;
    }

    public int getShipCounts() {
        return shipCounts;
    }

    public void setShipCounts(int shipCounts) {
        this.shipCounts = shipCounts;
    }
}